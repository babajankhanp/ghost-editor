const footer = '</body></html>';

export const copyHTMLToClipboard = () => {
  const targetElement = document.getElementById('templateWrapper').innerHTML;
  const html = head + targetElement + footer;
  if (targetElement) {
    const tempElement = document.createElement('textarea');
    tempElement.value = html;
    document.body.appendChild(tempElement);
    tempElement.select();
    document.execCommand('copy');
    document.body.removeChild(tempElement);
    alert('copied to clipbord');
  }
};

export const previewHtml = () => {
  const targetElement = document.getElementById('templateWrapper').innerHTML;
  const html = head + targetElement + footer;
  if (targetElement) {
    const tempElement = document.createElement('textarea');
    tempElement.value = html;
    document.body.appendChild(tempElement);
    tempElement.select();
    document.execCommand('copy');
    document.body.removeChild(tempElement);
    localStorage.setItem('previewHTML', html);
  }
};
